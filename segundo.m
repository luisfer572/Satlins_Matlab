function pushbutton1_Callback(hObject, eventdata, handles)

entrenamiento = get(handles.listbox2,'Value');

switch entrenamiento
    case 1
       handles.guardar = 'traingdx';
   
       guidata(hObject,handles);
    case 2
       handles.guardar = 'trainlm';
       
       guidata(hObject,handles);
     case 3
       handles.guardar = 'traingd';
       
       guidata(hObject,handles);
end

funcion = get(handles.listbox1,'Value');

switch funcion
    case 1
       handles.guardar0 = 'satlin';
   
       guidata(hObject,handles);
    case 2
       handles.guardar0 = 'satlins';
       
       guidata(hObject,handles);
end
fun=handles.guardar0;
algoritmo=handles.guardar;
%----------------------------------------
aux_1 = get(handles.capa,'String');
aux_2 =str2num(aux_1);
array_capas=[];
func_capa={};
recuperar_funcion = handles.guardar0;
global def
def={};
for n=1:aux_2
    
    texto_1='# NEURONAS PARA LA CAPA ';
    texto_2=num2str(n);
  if n==aux_2
    h = msgbox('Numero de neuronas 2');
    array_capas(aux_2)=2;

  else
      def = {'1'}; 
   texto_completo=[texto_1 texto_2];
    prompt = {texto_completo};
    dlg_title = '# DE NEURONAS POR CAPA';
    num_lines = 1;
    answer = inputdlg(prompt,dlg_title,num_lines,def);
   array_capas=[array_capas str2num(answer{1,1})];
  end
   func_capa(n)={fun};
 
end
array_capas
handles.num_capas = array_capas;
guidata(hObject,handles);
handles.func_capa_guardada = func_capa;
guidata(hObject,handles);

%prueba
%set(handles.txt_prueba_capas,'String',num2str(handles.num_capas));
%-------------------------------------------------------
a=str2double(get(handles.edit1,'String'));
b=str2double(get(handles.edit2,'String'));
c=str2double(get(handles.edit3,'String'));
d=str2double(get(handles.edit4,'String'));
a1=str2double(get(handles.edit6,'String'));
b1=str2double(get(handles.edit7,'String'));
c1=str2double(get(handles.edit8,'String'));
d1=str2double(get(handles.edit9,'String'));
a2=str2double(get(handles.edit10,'String'));
b2=str2double(get(handles.edit11,'String'));
c2=str2double(get(handles.edit12,'String'));
d2=str2double(get(handles.edit13,'String'));
%e=str2double(get(handles.capa,'String'));
funcion_de_la_capa = handles.func_capa_guardada;
%recuperar numero de capas con sus neuronas
numero_neuronas = handles.num_capas;

P=[a b c d; a1 b1 c1 d1];
T=[a2 b2 c2 d2];
W=max(max(P)); 
Z=min(min(P)); 
A=[W Z; W Z];
%set(handles.salir,'String',num2str(M));
net = newff([A], numero_neuronas, funcion_de_la_capa, algoritmo);
X=sim(net, P);
grid on
hold on
axes(handles.axes1);
plot(handles.axes1,P,T,P,X,'r');
view(net);


